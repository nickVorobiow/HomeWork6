package com.company;

public class SolutionDZ1 {
    public void task1() {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
    }

    public void task2() {
        for (int i = 10; i >= 1; i--) {
            System.out.print(i + " ");
        }
    }

    public void task3() {
        for (int i = 0; i <= 17; i++) {
            if (i % 2 == 0) {
                System.out.print(i + " ");
            }
        }
    }

    public void task4() {
        for (int i = 10; i >= -10; i--) {
            System.out.print(i + " ");
        }
    }
}
